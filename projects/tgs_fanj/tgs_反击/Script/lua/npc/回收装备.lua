local MainMenu =
[[
『$0080FF80|分解范围:黄龙系列、王陵系列、狐狸系列、极乐系列、太极系列』^^
<「游标.bmp」『$00FFFF00| 分解装备【将需要回收的装备放置物品栏第一格】』/@fenjie>^
<「游标.bmp」『$00FFFF00| 一键自动分解全部装备』/@fenjie2>^^
]];


function OnMenu(uSource, uDest)
   P_MenuSay(uSource, MainMenu);
 return;
end


function OnGetResult(uSource, uDest, aStr)
  if aStr == 'close' then
    return;
  end;

  if aStr == 'fanhui' then
    P_MenuSay(uSource, MainMenu);
    return;
  end;
  
  if aStr == 'fenjie2' then
    --判断密码
    if P_GetItemPassBo(uSource) then 
      P_MenuSay(uSource, '请先取消物品栏密码!');
      return;
    end	
	local Str = '';	
    for i = 0, 47 do
	  local ItemData = P_GetHaveItemInfoTabs(uSource, i);
	  if _fj[ItemData.Name] ~= nil and ItemData.Upgrade == 0 then
        Str = string.format('%s位置%d: %s^', Str, i, ItemData.Name);
	  end;  	
    end		
     if Str == '' then 
      P_MenuSay(uSource, '没有可分解道具!');
      return;
    end 
	Str = string.format('%s^<「游标.bmp」 『$00FFFF00|确认分解』/@qrfj2>', Str);
	Str = string.format('%s^<「游标.bmp」 『$00FFFF00|取消分解』/@exit>', Str);
    P_MenuSay(uSource, Str);
   return;
  end;

  if aStr == 'qrfj2' then
    --判断密码
    if P_GetItemPassBo(uSource) then 
      P_MenuSay(uSource, '请先取消物品栏密码!');
      return;
    end	
    for i = 0, 47 do
	  local ItemData = P_GetHaveItemInfoTabs(uSource, i);
	  if _fj[ItemData.Name] ~= nil and ItemData.Upgrade == 0 then
	    local t = _fj[ItemData.Name];
	    --判断物品栏
	    if P_getitemspace(uSource) < #t.set then
          P_MenuSay(uSource, string.format('物品栏请保留%d个空位!', #t.set));
          return;
	    end; 
	    --删除装备
		P_DelHaveItemInfo(uSource, i, 1);
	    --随机给予材料
	    local _Str = ''; -- 获取的道具字符
        for k, v in pairs(t.set) do	
          if type(v) == 'table' then
		    --给予道具
		    P_additem(uSource, v[1], v[2], '装备回收给予');
		    --记录给予的道具字符
		    _Str = string.format('%s[%d个%s] ', _Str, v[2], v[1]);
          end
        end;
	    P_saysystem(uSource, string.format('分解位置%d: %s,获得了: %s', i, ItemData.Name, _Str), 15);
	  end;  	
    end	
	P_MenuSay(uSource, '自动分解成功');
   return;
  end;

  if aStr == 'fenjie' then
    --判断密码
    if P_GetItemPassBo(uSource) then 
      P_MenuSay(uSource, '请先取消物品栏密码!');
      return;
    end	
    --获取第一个物品
	local ItemData = P_GetHaveItemInfoTabs(uSource, 0);
	if ItemData == nil then return end;
	if ItemData.Name == '' then
      P_MenuSay(uSource, '请将你需要回收的装备放置物品栏第一个格子!');
      return;
	end;
	--判断是否可回收道具
	if _fj[ItemData.Name] == nil then
      P_MenuSay(uSource, string.format('%s无法进行分解!', ItemData.ViewName));
      return;
	end;  	
	--判断强化等级
	if ItemData.Upgrade > 0 then 
           P_MenuSay(uSource, '只分解0段的装备!');
         return;
	end;  		
	--组成输出字符
	local Str = string.format('注意：你确定要分解装备【%s(强化%d级)】吗?^^', ItemData.Name, ItemData.Upgrade);	
	Str = string.format('%s回收后你将获得道具:^%s^', Str, _setitemstr(ItemData.Name));
	--判断精炼等级
	if ItemData.Upgrade > 0 then 
	  Str = string.format('%s%s^', Str, _levelstr(ItemData.Upgrade));
	end
	Str = string.format('%s^<「游标.bmp」 『$00FFFF00|确认分解』/@qrfj>', Str);
	Str = string.format('%s^<「游标.bmp」 『$00FFFF00|取消分解』/@exit>', Str);
    P_MenuSay(uSource, Str);
   return;
  end;

  if aStr == 'qrfj' then
    --判断密码
    if P_GetItemPassBo(uSource) then 
      P_MenuSay(uSource, '请先取消物品栏密码!');
      return;
    end	
    --获取第一个物品
	local ItemData = P_GetHaveItemInfoTabs(uSource, 0);
	if ItemData == nil then return end;
	if ItemData.Name == '' then
      P_MenuSay(uSource, '请将你需要回收的装备放置物品栏第一个格子!');
      return;
	end; 
	--判断是否可回收道具
	if _fj[ItemData.Name] == nil then
      P_MenuSay(uSource, string.format('%s无法分解回收!', ItemData.ViewName));
      return;
	end;  
       --判断强化等级
	if ItemData.Upgrade > 0 then 
           P_MenuSay(uSource, '只分解0段的装备!');
         return;
	end;  	
	local t = _fj[ItemData.Name];
	--判断物品栏
	if P_getitemspace(uSource) < #t.set then
      P_MenuSay(uSource, string.format('物品栏请保留%d个空位!', #t.set));
      return;
	end; 
	--删除装备
	P_deleteitem(uSource, ItemData.Name, 1, '装备分解删除');
	--随机给予材料
	local _Str = ''; -- 获取的道具字符
    for i, v in pairs(t.set) do	
      if type(v) == 'table' then
		--给予道具
		P_additem(uSource, v[1], v[2], '装备回收给予');
		--记录给予的道具字符
		_Str = string.format('%s[%d个%s]  ', _Str, v[2], v[1]);
      end
    end;
	--说话
	P_MenuSay(uSource, string.format('恭喜!!分解装备【%s】成功!!^^获得了: %s', ItemData.Name, _Str));	
   return;
  end;
end

function _setitemstr(ItemName)
  if ItemName == nil or _fj[ItemName] == nil then
   return '';
  end;
  local t = _fj[ItemName];
  local Str = '';
  for i, v in pairs(t.set) do
    if type(v) == 'table' then
      Str = string.format('%s[%d个%s]  ', Str, v[2], v[1]);
    end
  end;
  return Str;
end
