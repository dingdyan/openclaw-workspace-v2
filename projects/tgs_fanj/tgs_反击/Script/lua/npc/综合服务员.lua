--取随机率
function getRandRes(probArr)
  local total=0;
  for i=1,#probArr do
    total = total + probArr[i];
  end
  --math.randomseed(tostring(os.time()):reverse():sub(1, 6)) 
  local rand = math.random(total);
  for i=1,#probArr do
    if rand < probArr[i] then 
	  return i;
	else
	  rand = rand - probArr[i];
	end
  end
end;

--生成不重复随机
function GetRandomNumList(len, max)
  local tb = {}
  while #tb < len do 
    local istrue = false
    local num = math.random(1, max)
    if #tb ~= nil then
      for i = 1 ,#tb do
        if tb[i] == num then
          istrue = true
        end
      end
    end
    if istrue == false then
      table.insert( tb, num )
    end
  end
  table.sort(tb)
  return tb
end

