    P_MenuSay(uSource, string.format('ЕБЧАГЦКХО­бщ: %d', P_GetTempArr(uSource, 16)));
    return;
  end;
  
  if aStr == 'sj' then
    --ЛёШЁЕШМЖ
	local id = P_GetRcurid(uSource);
	--ЛёШЁЩ§МЖО­бщ
