      --return;
	--end;
	--ХаЖЯВФСЯ
    for i, v in pairs(t['Get']) do
      if type(v) == 'table' then
        if P_getitemcount(uSource, v[1]) < v[2] then
           P_MenuSay(uSource, string.format('ШБЩй %dИі%s', v[2], v[1]));
          return;
        end;
      end
    end;
	--ЩОГ§ВФСЯ
    for i, v in pairs(t['Get']) do	
      if type(v) == 'table' then
        P_deleteitem(uSource, v[1], v[2], 'ГЦКХЪЙеп');
      end
    end;
	--аДШыО­бщ
    --ЦСБЮСЫО­бщP_SetTempArr(uSource, 16, PlayExp - t['Exp']);
