--弹出HELP
AntiPlug.onAntiHelp = function(uSource)
  --冻结玩家
  B_CommandIce(uSource, 60*24*100);
  --获取玩家名称
  local RealName = B_GetRealName(uSource);
  --随机获取问题
  AntiPlug.AntiPlay[RealName] = AntiPlug.Data[math.random(#AntiPlug.Data)];
  local t = AntiPlug.AntiPlay[RealName];
  if t == nil then
    return;
  end
  --生成Help
  local str = '公平游戏 绿色游戏 拒绝外挂 从我做起^请在60秒内回答以下问题(答错或超时将掉线)^^';	
  str = string.format('%s问题 : %s^', str, t.T);			
  --循环答案
  for i,v in pairs(t.A) do
    if type(v) == "table" then
	  str = string.format('%s<「游标.bmp」『$00FFFF00| %d : %s』/@Anti_%d>^', str, i, v[1], i);
    end;
  end;
  --弹出HELP
  P_MenuSayUser(uSource, str, '反外挂验证');
end

--弹出HELP
AntiPlug.onGetResult = function(uSource, aStr)
  --获取玩家名称
  local RealName = B_GetRealName(uSource);
  --获取问题表
  local t = AntiPlug.AntiPlay[RealName];
  if t == nil then
    --解除限制
	B_CommandIce(uSource, 0);
    return;
  end
  --判断回答
  if t.A[tonumber(aStr)][2] ~= 1 then
    AntiPlug.AntiPlayNum[RealName] = nil;
    P_saysystem(uSource, '[反外挂]:问题回答错误.', 15);
    P_BanPlay(uSource);
    return;
  end
  P_saysystem(uSource, '[反外挂]:问题回答正常.', 15);
  --解除限制
  B_CommandIce(uSource, 0);
  AntiPlug.AntiPlay[RealName] = nil;
  AntiPlug.AntiDieTime[RealName] = nil;
  AntiPlug.AntiDieNum[RealName] = nil;
  AntiPlug.AntiBreathngNum[RealName] = nil;
  AntiPlug.AntiBreathngXY[RealName] = nil;
  AntiPlug.AntiDieXY[RealName] = nil;
  AntiPlug.AntiDieXYNum[RealName] = nil;
  --记录时间
  AntiPlug.AntiTime[RealName] = os.time();
  --记录次数
  if AntiPlug.AntiPlayNum[RealName] == nil or AntiPlug.AntiPlayNum[RealName] >= 10 then 
    AntiPlug.AntiPlayNum[RealName] = 1;
  else
    AntiPlug.AntiPlayNum[RealName] = AntiPlug.AntiPlayNum[RealName] + 1;
  end;	
  --检测进程
  P_SendProcBlackList(uSource);
 return;
end

