--原地心法检测
AntiPlug.OnBreathngMagicStart = function(uSource, MagicName)
  --检测地图
  local MapId, X, Y =  B_GetPosition(uSource);
  if AntiPlug.Map[MapId] == nil then 
    return;
  end;
  --获取玩家名称
  local RealName = B_GetRealName(uSource);
  --获取当前时间戳
  local t = os.time();
  --判断反外挂时间
  local _OnTime = AntiPlug._OnTime;
  if AntiPlug.AntiPlayNum[RealName] ~= nil then 
    _OnTime = _OnTime * (AntiPlug.AntiPlayNum[RealName] + 1);
  end;	
  if AntiPlug.AntiTime[RealName] ~= nil and AntiPlug.AntiTime[RealName] + _OnTime > t then
    return; 
  end;
  --判断心法坐标是否一样
  local XY = string.format('%d,%d', X, Y);
  if AntiPlug.AntiBreathngXY[RealName] ~= nil and AntiPlug.AntiBreathngXY[RealName] ~= XY then 
    AntiPlug.AntiBreathngXY[RealName] = nil;
	AntiPlug.AntiBreathngNum[RealName] = nil;
    return; 
  end;
  --记录开始心法坐标
  AntiPlug.AntiBreathngXY[RealName] = XY;
  --增加心法次数
  if AntiPlug.AntiBreathngNum[RealName] == nil then 
    AntiPlug.AntiBreathngNum[RealName] = 1;
  else
    AntiPlug.AntiBreathngNum[RealName] = AntiPlug.AntiBreathngNum[RealName] + 1;
  end;
  --P_saysystem(uSource, '心法次数:' .. AntiPlug.AntiBreathngNum[RealName], 2);
  --判断心法次数超过10次
  if AntiPlug.AntiBreathngNum[RealName] >= 10 then
    --60秒触发掉线
    P_SetEventTick(uSource, 98, 100 * 60);
    --弹出问答
	AntiPlug.onAntiHelp(uSource);
  end;	
  return;
end;

--回原点 死亡记录 
AntiPlug.OnKilled = function(uSource, MapId, X, Y)
  --检测地图
  if AntiPlug.Map[MapId] == nil then 
    return;
  end;
  --获取玩家名称
  local RealName = B_GetRealName(uSource);
  --获取问题表
  local t = AntiPlug.AntiPlay[RealName];
  if t ~= nil then
    --解除限制
    AntiPlug.AntiPlay[RealName] = nil;
	B_CommandIce(uSource, 0);
    return;
  end
  --获取当前时间戳
  local t = os.time();
  --判断反外挂时间
  local _OnTime = AntiPlug._OnTime;
  if AntiPlug.AntiPlayNum[RealName] ~= nil then 
    _OnTime = _OnTime * (AntiPlug.AntiPlayNum[RealName] + 1);
  end;	
  if AntiPlug.AntiTime[RealName] ~= nil and AntiPlug.AntiTime[RealName] + _OnTime > t then
    return; 
  end;
  --检测周边怪物
  local CheckMon = false;
  for k,v in ipairs(AntiPlug.KillMon) do
    local uObject, BoFind = B_FindObjectByName(uSource, v, 3);
    if BoFind then
      CheckMon = true;
	  break; 
    end;
  end
  if not CheckMon then
    AntiPlug.AntiDieXY[RealName] = nil;
    return; 
  end;
  --记录死亡坐标
  local XY = string.format('%d,%d', X, Y);
  AntiPlug.AntiDieXY[RealName] = XY;
  --P_saysystem(uSource, '死亡坐标:' .. AntiPlug.AntiDieXY[RealName], 2);
  return;
end;

--回原点 复活检测
AntiPlug.OnRevival = function(uSource, MapId, X, Y)
  --检测地图
  if AntiPlug.Map[MapId] == nil then 
    return;
  end;
  --P_saysystem(uSource,  string.format("%s复活了[%d,%d,%d]", B_GetRealName(uSource), MapId, X, Y), 20);
  --获取玩家名称
  local RealName = B_GetRealName(uSource);
  --检测是否有死亡记录
  if AntiPlug.AntiDieXY[RealName] == nil then 
    return;
  end;
  --判断坐标是否相同
  local XY = string.format('%d,%d', X, Y);
  if XY == AntiPlug.AntiDieXY[RealName] then 
    AntiPlug.AntiDieXY[RealName] = nil;
    AntiPlug.AntiDieXYNum[RealName] = nil;	
    return;
  end;
  --复活次数+1
  if AntiPlug.AntiDieXYNum[RealName] == nil then 
    AntiPlug.AntiDieXYNum[RealName] = 1;
  else
    AntiPlug.AntiDieXYNum[RealName] = AntiPlug.AntiDieXYNum[RealName] + 1;
  end;
  --P_saysystem(uSource, string.format("复活次数:%d", AntiPlug.AntiDieXYNum[RealName]), 20);
  --判断复活次数
  if AntiPlug.AntiDieXYNum[RealName] >= 10 then
    --弹出问答
	AntiPlug.onAntiHelp(uSource);
  end;	
  return;
end;

--进程检测
AntiPlug.CheckBlackList = function(uSource)
  --获取玩家基本属性
  local Attrib = P_GetAttrib(uSource);
  --30岁以下小号不检测
  if Attrib.Age <= 3000 then 
    return;
  end;
  --获取玩家名称
  local RealName = B_GetRealName(uSource);
  --获取当前时间戳
  local t = os.time();
  --判断反外挂时间
  if AntiPlug.AntiTime[RealName] ~= nil and AntiPlug.AntiTime[RealName] + 600 > t then
    return; 
  end;
  --检测进程
  P_SendProcBlackList(uSource);
  --检测窗口
  P_SendFormInfoList(uSource);
  --检测内存特征
  P_SignCodeInfoStringList(uSource);
  --记录时间
  AntiPlug.AntiTime[RealName] = t;
end;

