--物品爆出提示
function Equipmentbroke(uSource, MonsterName, Name, ViewName, Count)	
  if Name == nil then return end;
  local Item = DropItem[Name];
  if Item ~= nil then  
    local MapId, X, Y =  B_GetPosition(uSource);	
    M_worldnoticemsg(string.format('[%s]在%s的%d,%d被击败后掉落了%d个[%s]', MonsterName, M_GetMapName(MapId), X, Y, Count, ViewName), Item[1], Item[2]); 
    --M_worldnoticemsg(string.format('[%s]被[%s]击败掉落了%d个[%s]', MonsterName, P_getname(uSource),Count, ViewName), Item[1], Item[2]);	
  end;
end
