--判断装备
CheckWear.Get = function(uSource, uIndex, uNum)
  local n = 0;
  for k = 1, #CheckWear.Index do
	local ItemName = P_getitem(uSource, 1, 'rName', CheckWear.Index[k])
	if CheckWear.Item[ItemName] ~= nil and CheckWear.Item[ItemName] >= uIndex then 
	  n = n + 1;
	  if n >= uNum then 
	    return true;
	  end;
	end;
  end
  return false;
end