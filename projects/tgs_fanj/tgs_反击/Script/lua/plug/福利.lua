--福利触发配置
FuLi.Start = function(uSource, arg)
  --触发读取数据
  if arg[2] == '1' then 
    local Type = tonumber(arg[3]);
	if Type == nil then return end;
	FuLi.SendInfo(uSource, Type);
	FuLi.SendData(uSource, Type);
	--境界发送数量消息
	if Type == 3 then 
	  FuLi.SendEnergyNum(uSource);
	end;
  --触发领取请求
  elseif arg[2] == '2' then 
    local Type = tonumber(arg[3]);
	if Type == nil then return end;
    local ID = tonumber(arg[4]);
	if ID == nil then return end;
    local MapId, X, Y =  B_GetPosition(uSource);
	if MapId == 0 or MapId == 99 then 
      P_saysystem(uSource, '新手村禁止领取', 25);
      return;
	end;
	FuLi.PlayGet(uSource, Type, ID);
  end;
  
  return;
end;

--领取数据处理
FuLi.PlayGet = function(uSource, uType, uId)
  if FuLi.Item[uType] == nil then return end;
  if FuLi.Item[uType][uId] == nil then return end;
  --获取角色名称
  local PlayName = B_GetRealName(uSource);	
  --读取MYSQL数据
  local Data = M_GetMySqlDataSet(string.format('SELECT udata, utime FROM fuli_data WHERE playname=\'%s\' and utype = %d LIMIT 1', PlayName, uType));
  if Data == -1 then return end;
  local PlayData = {};
  local PlayTime = 0;
  --判断读取MYSQL状态
  if Data ~= nil and Data[1] ~= nil then
    PlayData = lua_Strtotable(Data[1]['udata'], '&');
	PlayTime = tonumber(Data[1]['utime']) or 0;
  else
    M_DoMySql(string.format('INSERT fuli_data (playname, utype) VALUES (\'%s\', %d);', PlayName, uType));
  end;
  --领取检测
  if uType == 0 then  --七日奖励
    --判断是否已领取
    if os.date("%Y-%m-%d", PlayTime) == os.date("%Y-%m-%d", os.time()) then 
      P_saysystem(uSource, '今日已领取过', 25);
      return;
    end;
	--判断是否已领取
	if PlayData[uId] == 1 then
      P_saysystem(uSource, '不能重复领取', 25);
      return;
    end; 
	--判断是否顺序领取
	if uId > 1 and PlayData[uId - 1] ~= 1 then 
      P_saysystem(uSource, '请按顺序领取', 25);
      return;
    end; 
  elseif uType == 1 then  --年龄奖励
	--判断是否已领取
	if PlayData[uId] == 1 then
      P_saysystem(uSource, '不能重复领取', 25);
      return;
    end;
	--判断年龄
	if P_GetAttrib(uSource).Age < FuLi.Item[uType][uId][3] then 
	  P_saysystem(uSource, string.format('年龄%0.2f才可领取', FuLi.Item[uType][uId][3] / 100), 25);
	  return;
	end; 
  elseif uType == 2 then  --签到
    --月份不同数据初始化
    if os.date("%m", PlayTime) ~= os.date("%m", os.time()) then 
	  PlayData = {};
	end;
    --判断是否当天
    if uId ~= tonumber(os.date('%d')) then 
      return;
    end;
	--判断是否已领取
	if PlayData[uId] == 1 then
      P_saysystem(uSource, '不能重复领取', 25);
      return;
    end;
  elseif uType == 3 then  --境界奖励
	--判断是否已领取
	if PlayData[uId] == 1 then
      P_saysystem(uSource, '不能重复领取', 25);
      return;
    end;
	--判断境界
	if P_GetEnergyLevel(uSource) < FuLi.Item[uType][uId][3] then 
	  P_saysystem(uSource, '境界不足', 25);
	  return;
	end; 
  else 
    return;
  end;
  --检测背包空位
  if P_getitemspace(uSource) < #FuLi.Item[uType][uId][2] then
    P_saysystem(uSource, '需要物品栏空位:' .. #FuLi.Item[uType][uId][2], 25);
    return;
  end;
  --改变奖励
  if #PlayData == 0 then 
    for id = 1, #FuLi.Item[uType] do
      table.insert(PlayData, 0);
    end;
  end;
  PlayData[uId] = 1;
  --拼接数据
  local udata = '';
  for id = 1, #PlayData do
    udata = udata .. PlayData[id] .. '\\&';
  end;
  --写入sql
  M_DoMySql(string.format('UPDATE fuli_data SET udata = \'%s\', utime = %d WHERE playname=\'%s\' and utype = %d', udata, os.time(), PlayName, uType));
  --给予道具
  for i, v in pairs(FuLi.Item[uType][uId][2]) do	
    if type(v) == 'table' then
      P_additem(uSource, v[1], v[2], '福利'..uType);
    end
  end;
  --返回提示
  --P_saysystem(uSource, '领取成功', 25);
  FuLi.SendData(uSource, uType);
  --境界处理
  if uType == 3 then
    FuLi.SetEnergyNum(uId);
    FuLi.SendEnergyNum(uSource);
  end;
end;

--发送数据文本
FuLi.SendInfo = function(uSource, uType)
  if FuLi.Item[uType] == nil then return end;
  --发送数据文本
  local Max = #FuLi.Item[uType];
  --签到特殊处理
  if uType == 2 then 
    Max = os.date("%d",os.time({year=os.date("%Y"),month=os.date("%m")+1,day=0}));
  end;
  for id = 1, Max do
    --拼接奖励文本
    local Str = '';
    for i, v in pairs(FuLi.Item[uType][id][2]) do
      if type(v) == 'table' then
        Str = string.format('%s%s:%d:%d:%d;', Str, v[1], v[2], v[3], v[4]);
      end
    end;
    P_saysystem(uSource, string.format('&hd;1;%d;%d&%s&%s', uType, id, FuLi.Item[uType][id][1], Str), 0);	
  end;
  --签到额外ID发送
  if uType == 2 then 
    --拼接奖励文本
	local id = 32;
    local Str = '';
    for i, v in pairs(FuLi.Item[uType][id][2]) do
      if type(v) == 'table' then
        Str = string.format('%s%s:%d:%d:%d;', Str, v[1], v[2], v[3], v[4]);
      end
    end;
    P_saysystem(uSource, string.format('&hd;1;%d;%d&%s&%s', uType, id, FuLi.Item[uType][id][1], Str), 0);
  end;
  
end;

--发送领取记录
FuLi.SendData = function(uSource, uType)
  if FuLi.Item[uType] == nil then return end;
  --获取角色名称
  local PlayName = B_GetRealName(uSource);
  --读取MYSQL数据
  local Data = M_GetMySqlDataSet(string.format('SELECT udata, utime FROM fuli_data WHERE playname=\'%s\' and utype = %d LIMIT 1', PlayName, uType));
  if Data == -1 then return end;
  --判断读取MYSQL状态
  if Data == nil or Data[1] == nil then
    return;
  end;
  --签到额外处理
  if uType == 2 then 
    if os.date("%m", Data[1]['utime']) ~= os.date("%m", os.time()) then 
	  return;
	end;
  end;
  P_saysystem(uSource, string.format('&hd;2;%d;%s', uType, Data[1]['udata']), 0);
end;

FuLi.SetEnergyNum = function(uId)
  --获取当前境界奖励存储数据
  local Data = M_GetMySqlDataSet('SELECT data FROM fuli_info WHERE id = 3');
  if Data == -1 then return end;
  local FLData = {};
  --判断读取MYSQL状态
  if Data ~= nil and Data[1] ~= nil then
    FLData = lua_Strtotable(Data[1]['data'], '&');
  end;
  --改变奖励
  if #FLData == 0 then 
    for id = 1, #FuLi.Item[3] do
      table.insert(FLData, 0);
    end;
  end;
  FLData[uId] = FLData[uId] + 1;
  --拼接数据
  local udata = '';
  for id = 1, #FLData do
    udata = udata .. FLData[id] .. '\\&';
  end;
  M_DoMySql(string.format('UPDATE fuli_info SET data = \'%s\' WHERE id = 3', udata));
  FuLi.EnergyStr = '';
end;

FuLi.SendEnergyNum = function(uSource)
  --获取当前境界奖励存储数据
  if FuLi.EnergyStr == '' then 
    local Data = M_GetMySqlDataSet('SELECT data FROM fuli_info WHERE id = 3');
    if Data == -1 then return end;
    local FLData = {};
    --判断读取MYSQL状态
    if Data ~= nil and Data[1] ~= nil then
      FLData = lua_Strtotable(Data[1]['data'], '&');
    end;
    for id = 1, #FuLi.Item[3] do
      if FLData[id] == nil then 
	    FLData[id] = 0;
	  end
      EnergyStr = string.format('%s%d:%d&', EnergyStr, FLData[id], FuLi.Item[3][id][4]);
    end;
  end;
  --发送数据文本
  P_saysystem(uSource, string.format('&hd;3;3;%s', EnergyStr), 0);
end;